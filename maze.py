from turtle import*

# Makes a single command to move to a location
def move(x, y):
    up()
    setposition(x, y)
    down()

# Draw filled square
def fillSquare(length):
    down()
    begin_fill()
    for i in range(4):
        forward(length)
        right(90)

    end_fill()
    up()

# Draw filled Star
def fillStar():
    pensize(2)
    down()
    begin_fill()
    for i in range(5):
        forward(30)
        right(144)
    end_fill()
    up()

# Draw filled Circle
def fillCircle(data):
    for spot in data:
        setposition(spot[0],spot[1])
        down()
        begin_fill()
        circle(spot[2])
        end_fill()
        up()

